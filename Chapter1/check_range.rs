fn main() {
    println!("--- 符号あり整数 ---");
    println!("i8={}~{}", i8::MIN, i8::MAX);
    println!("i16={}~{}", i16::MIN, i16::MAX);
    println!("i32={}~{}", i32::MIN, i32::MAX);
    println!("i64={}~{}", i64::MIN, i64::MAX);

    println!("--- 符号なし整数 ---");
    println!("u8={}~{}", u8::MIN, u8::MAX);
}